function Vector(x, y) {
	this.x = x;
	this.y = y;
}

Vector.prototype.plus = function(other) {
	return new Vector(this.x + other.x, this.y + other.y);
};

Vector.prototype.times = function(scale) {
	return new Vector(this.x * scale, this.y * scale);
};

//使用符号表示游戏内内容
var actorchars = {
    "@": Player,  //玩家角色
    "0": Monster,   //小怪
    "=": Monster,  //移动怪
    "B": Monster,  //Boss怪
    "*": Entry, //入口
    "$": TransparentWall, //透明墙
    "#": NPC, //NPC
    "!": House  //屋子
};

function Player(pos) { 
	this.pos = new Vector(0,0);  //玩家起始位置
	this.size = new Vector(3, 3); //玩家角色大小
	this.speed = new Vector(0, 0); 
}
Player.prototype.type = "player";

function Monster(pos, ch) {
	this.pos = pos;
	this.size = new Vector(1, 1);
	if (ch === "=")
		this.speed = new Vector(2, 0);//控制方块自动移动方向速度（上下，左右）
	else if (ch === 'B')
		this.size = new Vector(3, 3);
}
Monster.prototype.type = "monster";

function Entry(pos) {
	this.basePos = this.pos = pos;
	this.size = new Vector(1, 1); //大小
}
Entry.prototype.type = "entry";

Map.prototype.isFinished = function() {
	return this.status != null && this.finishDelay < 0;  //判断关卡通过，进入下一关
};


function Map(plan) { 
	this.width = plan[0].length;
	this.height = plan.length;
	this.grid = [];
	this.actors = [];

	for (var y = 0; y < this.height; y++) {
	    var line = plan[y], gridLine = [];

		for (var x = 0; x < this.width; x++) {
			var ch = line[x],fieldType = null;
			var Actor = actorchars[ch];

			if (Actor)
			    this.actors.push(new Actor(new Vector(x, y), ch));
			else if (ch === "&")
			    fieldType = "tree";
			else if (ch === "0")
			    fieldType = "monster";
			else if (ch === "=")
			    fieldType = "monster";
			else if (ch === "B")
			    fieldType = "monster";
			else if (ch === "*")
			    fieldType = "entry";
			else if (ch === "$")
			    fieldType = "transparentwall";
			else if (ch === "#")
			    fieldType = "npc";
			else if (ch === "!")
			    fieldType = "house";
			gridLine.push(fieldType)
		}
		this.grid.push(gridLine);
	}
	this.player = this.actors.filter(function(actor) {
		return actor.type === "player"
	})[0];
	this.status = this.finishDelay = null;
}

function element(name, className) {
	var elem = document.createElement(name);
	if (className)
	    elem.className = className;
	return elem;
}

function DOMDisplay(parent, map) {
	this.wrap = parent.appendChild(element("div", "game"));
	this.map = map;

	this.wrap.appendChild(this.drawBackground());
	this.actorLayer = null;
	this.drawFrame();
}

var scale = 15;

DOMDisplay.prototype.drawBackground = function() {  //两层画布
	var table = element("table", "background");
	table.style.width = this.map.width * scale + "px";
	table.style.height = this.map.height * scale + "px";
	this.map.grid.forEach(function(row) {
		var rowElement = table.appendChild(element("tr"));
		rowElement.style.height = scale + "px";
		row.forEach(function(type) {
			rowElement.appendChild(element("td", type));
		});
	});
	return table;
};

DOMDisplay.prototype.drawActors = function() {  //人物活动范围
	var wrap = element("div");
	this.map.actors.forEach(function(actor) {
		var rect = wrap.appendChild(element("div", "actor " + actor.type));
		rect.style.width = actor.size.x * scale + "px";
		rect.style.height = actor.size.y * scale + "px";
		rect.style.left = actor.pos.x * scale + "px";
		rect.style.top = actor.pos.y * scale + "px";
	});
	return wrap;
}

DOMDisplay.prototype.drawFrame = function() { //
	if (this.actorLayer)
		this.wrap.removeChild(this.actorLayer);
	this.actorLayer = this.wrap.appendChild(this.drawActors());
	this.wrap.className = "game " + (this.map.status || "");
	this.scrollPlayerIntoView();  //跟随人物
};

// clear it later
DOMDisplay.prototype.scrollPlayerIntoView = function() {
	var width = this.wrap.clientWidth;
	var height = this.wrap.clientHeight;
	var margin = width / 3;  //视野范围

	// The viewport
	var left = this.wrap.scrollLeft,
		right = left + width;
	var top = this.wrap.scrollTop,
		bottom = top + height;

	var player = this.map.player;
	var center = player.pos.plus(player.size.times(0.5))
		.times(scale);
    //定义人物能走到的范围
	if (center.x < left + margin)
		this.wrap.scrollLeft = center.x - margin;
	else if (center.x > right - margin)
		this.wrap.scrollLeft = center.x + margin - width;
	if (center.y < top + margin)
		this.wrap.scrollTop = center.y - margin;
	else if (center.y > bottom - margin)
		this.wrap.scrollTop = center.y + margin - height;
};

DOMDisplay.prototype.clear = function() {  //清除界面，转换之前
	this.wrap.parentNode.removeChild(this.wrap);
};
