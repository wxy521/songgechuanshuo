Map.prototype.obstacleAt = function(pos, size) {  //地图坐标
	var xStart = Math.floor(pos.x); //x轴起始
	var xEnd = Math.ceil(pos.x + size.x); //x轴结束
	var yStart = Math.floor(pos.y);  //y轴起始
	var yEnd = Math.ceil(pos.y + size.y);  //y轴结束

	if (xStart < 0 || xEnd > this.width || yStart < 0 || yEnd > this.height) //出地图边界
		return "tree";
	for (var y = yStart; y < yEnd; y++) {
		for (var x = xStart; x < xEnd; x++) {
			var fieldType = this.grid[y][x];
			if (fieldType)
			    return fieldType;
		}
	}
};
//人物运动碰撞
Map.prototype.actorAt = function(actor) {
	for (var i = 0; i < this.actors.length; i++) {
		var other = this.actors[i];
		if (other != actor &&
			actor.pos.x + actor.size.x > other.pos.x &&
			actor.pos.x < other.pos.x + other.size.x &&
			actor.pos.y + actor.size.y > other.pos.y &&
			actor.pos.y < other.pos.y + other.size.y)
			return other;
	}
};

var maxStep = 0.05;

Map.prototype.animate = function(step, keys) {
	if (this.status != null)
		this.finishDelay -= step;

	while (step > 0) {
		var thisStep = Math.min(step, maxStep);
		this.actors.forEach(function(actor) {
			actor.act(thisStep, this, keys);
		}, this);
		step -= thisStep;
	}
};

Monster.prototype.act = function(step, map) {
	var newPos = this.pos.plus(this.speed.times(step));     //首先，计算移动后的位置；
	if (!map.obstacleAt(newPos, this.size))   //然后，判断是否碰到别的物体，如果没碰到，移动到下一个位置。
		this.pos = newPos;               //如果碰到了，对于滴落的monster，它有个属性repeatPos（初始位置），直接移动到repeatPos；
	else if (this.repeatPos)             //没有repeatPos属性的monster，都可以往复运动，撞墙之后弹回，即speed逆转。
		this.pos = this.repeatPos;
	else
		this.speed = this.speed.times(-1);
};

var wobbleSpeed = 8;

Entry.prototype.act = function(step) {
	this.wobble += step * wobbleSpeed;
	this.pos = this.basePos.plus(new Vector(0, 0));
};

var playerXSpeed = 10;
var playerYSpeed = 10;

Player.prototype.moveX = function(step, map, keys) {
	this.speed.x = 0;
	if (keys.left) this.speed.x -= playerXSpeed;
	if (keys.right)this.speed.x += playerXSpeed;

	var motion = new Vector(this.speed.x * step, 0);
	var newPos = this.pos.plus(motion);
	var obstacle = map.obstacleAt(newPos, this.size);
	if (obstacle)
		map.playerTouched(obstacle);
	else
		this.pos = newPos;
};

Player.prototype.moveY = function(step, map, keys) {
	this.speed.y = 0;
	if (keys.up) this.speed.y -=  playerYSpeed;
	if (keys.down) this.speed.y += playerYSpeed;
	var motion = new Vector(0,this.speed.y * step);
	var newPos = this.pos.plus(motion);
	var obstacle = map.obstacleAt(newPos, this.size);
	if (obstacle)
		map.playerTouched(obstacle);
	else
		this.pos = newPos;
};

//主要分成四部分：
//1. 水平方向移动 moveX()；
//2. 垂直方向移动 moveY()；
//3. 碰撞检测 Map.playerTouched() ；
//4. 游戏失败处理 lost 。
Player.prototype.act = function(step, map, keys) {
	this.moveX(step, map, keys);
	this.moveY(step, map, keys);

	var otherActor = map.actorAt(this);
	if (otherActor)
		map.playerTouched(otherActor.type, otherActor);


	if (map.status == "lost") {
		this.pos.y += step;
		this.size.y -= step;
	}
};
//碰撞检测
//如果碰到boss、smonster、movemonster进行战斗判定，若胜则继续，若负则结束游戏；
//如果碰到entry则进入下一地图；
//如果碰到NPC则进行对话；
//如果碰到house则进入房子地图
Map.prototype.playerTouched = function(type, actor) {
    if (type == "monster") {
        this.actors = this.actors.filter(function (other) {
            return other != actor;
        });
        alert("加入战斗");
    } else if (type == "entry") {
        this.status = "won";
        this.finishDelay = 1;
        alert("进入下一关");
    } else if (type == "npc") {
        alert("与NPC对话");
    } else if (type == "door") {
        this.status = "in"; //新增status进入房子
        this.finishDelay = 1;
    }
};

var arrowCodes = {
	37: "left",
	38: "up",
	39: "right",
	40: "down"
};

function trackKeys(codes) {
	var pressed = Object.create(null);

	function handler(event) {
		if (codes.hasOwnProperty(event.keyCode)) {
			var down = event.type == "keydown";
			pressed[codes[event.keyCode]] = down;
			event.preventDefault();
		}
	}
	addEventListener("keydown", handler);
	addEventListener("keyup", handler);
	return pressed;
}

function runAnimation(frameFunc) {
	var lastTime = null;

	function frame(time) {
		var stop = false;
		if (lastTime != null) {
			var timeStep = Math.min(time - lastTime, 100) / 1000;
			stop = frameFunc(timeStep) === false;
		}
		lastTime = time;
		if (!stop)
			requestAnimationFrame(frame);
	}
	requestAnimationFrame(frame);
}

var arrows = trackKeys(arrowCodes);

function runLevel(map, Display, andThen) {
	var display = new Display(document.body, map);
	runAnimation(function(step) {
		map.animate(step, arrows);
		display.drawFrame(step);
		if (map.isFinished()) {
			display.clear();
			if (andThen)
				andThen(map.status);
			return false;
		}
	});
}

function runGame(plans, Display) {  
    var lifeCount = 3;  
    function startLevel(n) {  
        runLevel(new Map(plans[n]), Display, function(status) {  
            if (status == "lost") {  
                lifeCount --;  
                if(lifeCount > 0) {  
                    startLevel(n);  
                }  
                else {  
                    startLevel(0);  
                }  
            }  
            else if (n < plans.length - 1)  
                startLevel(n + 1);  
            else  
                alert("You win!");  
        });  
    }  
    startLevel(0);  
}  

